"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import type { Profile, Role } from "@/types";

interface SidebarProps {
  profile: Profile | null;
  isOpen: boolean;
  onClose: () => void;
}

interface NavItem {
  href: string;
  label: string;
  icon: React.ReactNode;
  roles: Role[];
}

export function Sidebar({ profile, isOpen, onClose }: SidebarProps) {
  const pathname = usePathname();

  const navItems: NavItem[] = [
    {
      href: "/",
      label: "Dashboard",
      icon: <DashboardIcon />,
      roles: [
        "maestro",
        "revisor",
        "coordinador_municipal",
        "coordinador_puesto",
        "testigo",
      ],
    },
    {
      href: "/admin",
      label: "Administración",
      icon: <AdminIcon />,
      roles: ["maestro"],
    },
  ];

  const visibleItems = navItems.filter(
    (item) => profile?.role && item.roles.includes(profile.role),
  );

  return (
    <aside
      className={`
        fixed inset-y-0 left-0 z-50 w-64 bg-white border-r border-gray-200 flex flex-col
        transform transition-transform duration-300 ease-in-out
        ${isOpen ? "translate-x-0" : "-translate-x-full"}
        nav:static nav:translate-x-0 nav:w-64
      `}
    >
      <div className="p-6 border-b border-gray-200">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-blue-600 rounded-lg flex items-center justify-center">
            <span className="text-white font-bold text-lg">E</span>
          </div>
          <div>
            <h1 className="font-bold text-gray-900">E-14 Digital</h1>
            <p className="text-xs text-gray-500">Casanare 2026</p>
          </div>
        </div>
      </div>

      <nav className="flex-1 p-4 space-y-1">
        {visibleItems.map((item) => {
          const isActive =
            pathname === item.href || pathname.startsWith(`${item.href}/`);

          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                isActive
                  ? "bg-blue-50 text-blue-700"
                  : "text-gray-600 hover:bg-gray-50 hover:text-gray-900"
              }`}
            >
              <span className={isActive ? "text-blue-600" : "text-gray-400"}>
                {item.icon}
              </span>
              <span className="font-medium">{item.label}</span>
            </Link>
          );
        })}
      </nav>

      <div className="p-4 border-t border-gray-200">
        <div className="text-xs text-gray-500 text-center">
          <p>© 2026 E-14 Digital</p>
          <p className="mt-1">Versión 1.0.0</p>
        </div>
      </div>
    </aside>
  );
}

